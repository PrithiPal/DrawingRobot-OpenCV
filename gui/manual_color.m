function change_color(handle, n)
    I = imread('Color_to_process.bmp');
    PEN_DATA = csvread('color_order.csv');
    C = PEN_DATA(n,:);


    color_patch = ones([size(I,1) size(I,2) 3]);
    color_patch(:,:,1) = C(1,1)./255;
    color_patch(:,:,2) = C(1,2)./255;
    color_patch(:,:,3) = C(1,3)./255;

    color_overlap = color_patch;
    r = color_patch(:,:,1);
    g = color_patch(:,:,2);
    b = color_patch(:,:,3);

    r(I==0)=0;
    g(I==0)=0;
    b(I==0)=0;

    %figure,subplot(1,2,1),imshow(r);

    color_patch(:,:,1) = r;
    color_patch(:,:,2) = g;
    color_patch(:,:,3) = b;

    axes(handle);
    imshow(color_patch);
    imwrite(color_patch, 'temp.bmp')



